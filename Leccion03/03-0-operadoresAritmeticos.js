let a = 3;
let b = 2;

let z = a + b;
console.log("resultado de la suma: " + z);

z = a - b;
console.log("resultado de la resta: " + z);

z = a * b;
console.log("resultado de la multiplicacion: " + z);

z = a / b;
console.log("resultado de la division: " + z);

z = a % b;
console.log("resultado de la operacion modulo: " + z);

z = a ** b;
console.log("resultado de operador exponente: " + z);

//Incremento
//Pre-incremento (el operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

//Post-incremento (el operador ++ despues de la variable)
z = b++;
console.log(b);
console.log(z);


//Decremento
//Pre-decremento (el operador -- antes de la variable)
z = --a;
console.log(a);
console.log(z);

//Post-decremento (el operador -- despues de la variable)
z = b--;
console.log(b);
console.log(z);